class Cliente {
  constructor(nome, email, cpf, telefone, endereco, cidade, estado, cep, dataNascimento) {
    this.nome = nome;
    this.email = email;
    this.cpf = cpf;
    this.telefone = telefone;
    this.endereco = endereco;
    this.cidade = cidade;
    this.estado = estado;
    this.cep = cep;
    this.dataNascimento = dataNascimento;
  }
}

export default Cliente;
